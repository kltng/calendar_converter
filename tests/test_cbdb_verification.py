@pytest.mark.parametrize("era,year_in_era,expected_year", CBDB_SAMPLES)
def test_cbdb_nianhao(db, era, year_in_era, expected_year):
    """Verify era + year_in_era resolves to expected Gregorian year.

    Many eras start mid-year, so year 1 may lack month 1. We query without
    month/day and check that the expected Gregorian year appears among
    the returned months.
    """
    parsed = ParsedDate(era=era, year=year_in_era, country_hint="chinese")
    results = convert_cjk_to_jdn(db, parsed)
    assert len(results) >= 1, f"No results for {era} year {year_in_era}"
    greg_years = {jdn_to_gregorian(jdn)[0] for jdn, _ in results}
    assert expected_year in greg_years, (
        f"{era}{year_in_era}年: expected {expected_year}, "
        f"got {sorted(greg_years)}"
    )
